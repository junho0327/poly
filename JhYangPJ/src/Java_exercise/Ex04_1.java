package Java_exercise;

public class Ex04_1 {

	public static void main(String[] args) {
		int a, b = 5, c = 3;

		a = b + c;
		System.out.println(b + " + " + c + " = " + a);

		a = b - c;
		System.out.println(b + " - " + c + " = " + a);

		a = b * c;
		System.out.println(b + " * " + c + " = " + a);

		a = b / c;
		System.out.println(b + " / " + c + " = " + a);

		a = b % c;
		System.out.println(b + " % " + c + " = " + a);

	}

}
